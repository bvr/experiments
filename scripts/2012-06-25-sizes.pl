
use Path::Class;
use Table::Builder;
use List::Util 'sum';
use Number::Format 'format_bytes';

my $files_table = Table::Builder->new(cols => [
    'Name',
    'Size' => { align => 'right', formatter => sub { format_bytes($_) } },
]);

my $tool_dir = dir("d:\\DataDict\\787Tools\\ConstDict\\Verification\\trunk");
while(<DATA>) {
    chomp;
    if(/^\+\+\+ (.*)$/) {
        my $file = -e file($1) ? file($1) : $tool_dir->file($1);

        unless(-e $file) {
            warn "+++ Cannot find $1\n";
            next;
        }
        $files_table->add_row($file->absolute, $file->stat->size);
    }
}

$files_table->sort_rows(sub { my ($a,$b) = @_;  $b->Size <=> $a->Size });
$files_table
    ->add_sep
    ->add_summary_row('Total', sub { sum @_  });
print $files_table->render_as('ascii');


