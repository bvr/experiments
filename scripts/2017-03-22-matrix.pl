sub fill_matrix {
    my ($mat, $fill) = @_;

    if($count++ % 100_000 == 0) {
        warn $count, "\n";
        warn visual_matrix($mat), "-"x30, "\n";
    }

    # matrix complete
    if($fill == 7*7) {
        warn "="x30,visual_matrix($mat),"="x30,"\n"
            if is_valid($mat);
        return;
    }

    # addition
    my @options = find_options_for_add($mat, $fill);
    for my $val (@options) {
        my $new_mat = [@{$mat}[0..$fill-1], $val];
        my $valid = is_valid($new_mat);
        # dd [ $valid, $new_mat ];
        next if defined($valid) && $valid == 0;
        fill_matrix($new_mat, $fill+1);
    }
}

sub find_options_for_add {
    my ($mat, $idx) = @_;

    my $row = int($idx / 7);
    my $col =     $idx % 7;

    # here are the sums
    return (0) if $row ~~ [1,3,5] && $col ~~ [1,3,5];

    my @indexes = (( map { $row*7 + $_ } 0..6 ), ( map { $_*7 + $col } 0..6 ));
    my @used    = map { defined $mat->[$_] ? ($mat->[$_]): () } @indexes;

    return (grep { ! ($_ ~~ @used) } (1..7));
}

sub visual_matrix {
    my $mat = shift;

    my $out = "";
    for my $row (0..6) {
        for my $col (0..6) {
            $out .= sprintf " %2d ", defined $mat->[$row*7 + $col] ? $mat->[$row*7 + $col] : 0;
        }
        $out .= "\n";
    }
    return $out;
}

sub is_valid {
    my $mat = shift;

    my @tests = (
        { test => sub { sum_square($mat, 1,1) }, result => 26 },
        { test => sub { sum_square($mat, 1,3) }, result => 35 },
        { test => sub { sum_square($mat, 1,5) }, result => 37 },
        { test => sub { sum_square($mat, 3,1) }, result => 31 },
        { test => sub { sum_square($mat, 3,3) }, result => 36 },
        { test => sub { sum_square($mat, 3,5) }, result => 39 },
        { test => sub { sum_square($mat, 5,1) }, result => 38 },
        { test => sub { sum_square($mat, 5,3) }, result => 26 },
        { test => sub { sum_square($mat, 5,5) }, result => 24 },
    );
    my $all_defined = 1;
    for my $t (@tests) {
        my $res = $t->{test}->();
        if(defined $res) {
            return 0 if $res != $t->{result};
        }
        else {
            $all_defined = 0;
        }
    }

    return 1 if $all_defined;
    return;
}

sub sum_square {
    my ($mat, $row, $col) = @_;
    my @indexes = (
        ($row-1)*7 + $col-1, ($row-1)*7 + $col, ($row-1)*7 + $col+1,
         $row   *7 + $col-1,                     $row   *7 + $col+1,
        ($row+1)*7 + $col-1, ($row+1)*7 + $col, ($row+1)*7 + $col+1,
    );

    return unless all { defined } @{$mat}[@indexes];

    return sum @{$mat}[@indexes];
}

